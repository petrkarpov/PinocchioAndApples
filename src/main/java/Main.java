public class Main {
    public static void main(String[] args) {
        final int pinocchio = 10;
        final int karabas = 8;
        final int PinocchioRemainder = pinocchio - karabas;

        System.out.println("Кол-во яблок у Буратино: " + pinocchio);
        System.out.println("Кол-во яблок украденные Карабасом-Барабасом: " + karabas);
        System.out.println("Кол-во яблок осталось у Буратино: " + PinocchioRemainder);
    }
}